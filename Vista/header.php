                          <div class="modal-body">
                             <p>Los objetivos estratégicos del Ministerio de acuerdo al plan de la patria 2013-2019 son:
                              Eliminar definitivamente el latifundio. Realizar un proceso de organización y zonificación agro ecológica en base a las capacidades de uso de la tierra y crear un sistema de catastro rural para garantizar el acceso justo y uso racional del suelo.
                              Acelerar la democratización del acceso de los campesinos y campesinas, productores y productoras, y de las distintas formas colectivas y empresas socialistas, a los recursos necesarios para la producción (tierra, agua, riego, semillas y capital), impulsando el uso racional y sostenible de los mismos.</p>

                                                            <p>Afianzar un conjunto de políticas públicas de apoyo a la producción, comercialización y organización del sector rural y participación del poder popular campesino en la implementación de un Plan Nacional de Producción de Alimentos que garantice la soberanía alimentaria.
                              Fortalecer la infraestructura, el desarrollo y funcionamiento de los grandes polos socialistas de producción primaria agropecuaria y grandes sistemas de riego, gestionados a través de empresas socialistas, privilegiando la integración de los procesos productivos a escala industrial.</p>

                              <p>Consolidar las redes de producción y distribución de productos de consumo directo y del sistema de procesamiento agroindustrial.
Crear, consolidar y apoyar centros de venta y distribución directa de productos agropecuarios y otros de consumo masivo, locales y en las grandes ciudades, garantizando su acceso a precio justo por parte de la población y una remuneración justa al trabajo campesino e incentivando el desarrollo del comercio local, nacional y de exportación.
Consolidar el aparato agroindustrial bajo control de empresas socialistas, garantizando al menos un 60% de la capacidad de almacenamiento y procesamiento en rubros básicos (cereales, oleaginosas, leguminosas, azúcar, carne y leche) y un 30% en el resto de los rubros alimenticios.</p>

                          <p>Desarrollar un sistema de apoyo e incentivos para la promoción del comercio internacional de exportación de rubros agrícolas.
Establecer mecanismos para ejercer la nueva institucionalidad revolucionaria que garantice la participación de los pequeños y medianos productores en las decisiones en materia agropecuaria, a través de los consejos campesinos y las redes de productores y productoras libres y asociados.</p>

